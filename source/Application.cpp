void Application::CreateInstance()
{
    uint32_t glfwExtensionCount = 0;
    const char **glfwExtensions;

    if (!CheckGLFWRequired(glfwExtensionCount, glfwExtensions))
        throw std::runtime_error("failed to match requirement of GLFW extension!");

    if (enableValidationLayers && !CheckValidationLayerSupport())
        throw std::runtime_error("failed to match requirement of validation layer!");
    std::vector<const char *> requiredExtensions(glfwExtensions, glfwExtensions + glfwExtensionCount);
    // c-style api
    VkApplicationInfo appInfo{};
    appInfo.sType = VK_STRUCTURE_TYPE_APPLICATION_INFO;
    appInfo.pApplicationName = "Hellow Triangle";
    appInfo.applicationVersion = VK_MAKE_VERSION(1, 0, 0);
    appInfo.pEngineName = "No Engine";

    appInfo.engineVersion = VK_MAKE_VERSION(1, 0, 0);
    appInfo.apiVersion = VK_API_VERSION_1_0;

    VkInstanceCreateInfo createInfo{};
    createInfo.sType = VK_STRUCTURE_TYPE_INSTANCE_CREATE_INFO;
    createInfo.pApplicationInfo = &appInfo;

    VkDebugUtilsMessengerCreateInfoEXT debugCreateInfo{};

    if (enableValidationLayers)
    {
        requiredExtensions.push_back(VK_EXT_DEBUG_UTILS_EXTENSION_NAME);
        createInfo.enabledLayerCount = static_cast<uint32_t>(validationLayers.size());
        createInfo.ppEnabledLayerNames = validationLayers.data();

        PopulateDebugMessengerCreateInfo(debugCreateInfo);
        createInfo.pNext = &debugCreateInfo;
    }
    else
    {
        createInfo.enabledLayerCount = 0;
        createInfo.pNext = nullptr;
    }

    createInfo.enabledExtensionCount = static_cast<uint32_t>(requiredExtensions.size());
    createInfo.ppEnabledExtensionNames = requiredExtensions.data();

    if (vkCreateInstance(&createInfo, nullptr, &Instance))
    {
        throw std::runtime_error("failed to create vk instance!");
    }
}

bool Application::CheckGLFWRequired(uint32_t &count, const char **&extensions)
{
    uint32_t extensionCount = 0;
    vkEnumerateInstanceExtensionProperties(nullptr, &extensionCount, nullptr);
    std::vector<VkExtensionProperties> supportedExtension(extensionCount);
    vkEnumerateInstanceExtensionProperties(nullptr, &extensionCount, supportedExtension.data());

    extensions = glfwGetRequiredInstanceExtensions(&count);

    for (size_t i = 0; i < count; i++)
    {
        bool isMatch = false;
        for (size_t j = 0; j < extensionCount; j++)
        {
            if (std::strcmp(supportedExtension[j].extensionName, extensions[i]))
            {
                isMatch = true;
                break;
            }
        }

        if (!isMatch)
            return false;
    }

    return true;
}

